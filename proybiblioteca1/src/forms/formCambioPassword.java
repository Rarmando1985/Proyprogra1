    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        String nuevopasword= txtNuevoPassword.getText();
        String confirmacion= txtConfirmacion.getText();
        
        if (nuevopasword.trim().isEmpty()) {
        JOptionPane.showMessageDialog(null, "El nuevo password no puede estar vacío.");
        } else if (confirmacion.trim().isEmpty()) {
            JOptionPane.showMessageDialog(null, "La confirmación de password no puede estar vacía.");
        } else {
            if (nuevopasword.equals(confirmacion)) {
                actualizarpassword();
                limpiarcampos();
            } else {
                JOptionPane.showMessageDialog(null, "El nuevo password y la confirmación son diferentes.");
            }
        }

        
    }//GEN-LAST:event_btnGuardarActionPerformed

