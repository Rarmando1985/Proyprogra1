    public void actualizarpassword(){
        try {
        String usuario= txtUsuario.getText();
        String nuevoPassword= txtNuevoPassword.getText();
        String confirmacion= txtConfirmacion.getText();
        Conexion cc = new Conexion();
        Connection cn = cc.conectar();
        
          String sashsalt=nuevoPassword.toUpperCase()+userRol.toUpperCase();
          String has = HashUtil.sha256(sashsalt);

        String sql = "UPDATE usuarios SET password_salt = ?, password_hash = ? WHERE LOWER(usuario) = LOWER(?)";
        PreparedStatement ps = cn.prepareStatement(sql);
        ps.setString(1, has);  
        ps.setString(2, has);  
        ps.setString(3, usuario);

        int filas = ps.executeUpdate();
        if (filas > 0) {
            JOptionPane.showMessageDialog(null, "Contraseña actualizada correctamente.");
        } else {
            JOptionPane.showMessageDialog(null, "No se encontró el usuario o no se pudo actualizar.");
        }

    } catch (Exception ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Error al actualizar la contraseña: " + ex.getMessage());
    }
    }
    
